a = 4
b = 2

# Operaciones booleanas

# Igualdad
resultado = (a == b)
print(resultado)

# Diferencia
resultado = a != b
print(resultado)

# Mayor que
resultado = a > b
print(resultado)

# Mayor o igual que
resultado = a >= b
print(resultado)

# Menor o igual que
resultado = a <= b
print(resultado)

# Residuo para saber si el numero es par o impar
if a % 2 == 0:
    print("Es numero par")
else:
    print("Es numero impar")
